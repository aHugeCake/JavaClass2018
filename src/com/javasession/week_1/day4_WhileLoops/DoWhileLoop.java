package com.javasession.week_1.day4_WhileLoops;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		/*
		 * 2. do while loop
		 * 
		 * 		do
		 * 		{
		 * 			statement(s);
		 * 		}while(condition);
		 * 
		 */
		
		int i = 10;
		
		do {
			System.out.println(i);
			i--;
		}while(i > 11);
	}
	
}
