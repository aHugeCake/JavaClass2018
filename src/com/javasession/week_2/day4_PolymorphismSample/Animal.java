package com.javasession.week_2.day4_PolymorphismSample;

public class Animal {
	
	//Inheritance --> child class uses father's methods
	//polymorhpism --> father class uses child's methods
	public void sound() {
		System.out.println("Sound from animal class");
	}
	
	public void move() {
		System.out.println("Move from animal class");
	}
	

}
